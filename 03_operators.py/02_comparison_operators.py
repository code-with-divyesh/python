
# Take input from user
num1 = float(input("Enter first number: "))
num2 = float(input("Enter second number: "))

# Equal to
print(f"{num1} == {num2} :", num1 == num2)

# Not equal to
print(f"{num1} != {num2} :", num1 != num2)

# Greater than
print(f"{num1} > {num2} :", num1 > num2)

# Less than
print(f"{num1} < {num2} :", num1 < num2)

# Greater than or equal to
print(f"{num1} >= {num2} :", num1 >= num2)

# Less than or equal to
print(f"{num1} <= {num2} :", num1 <= num2)
